// File ThreeLoopTest.java
public class ThreeLoopTest {

    public static void main(String args[]) {
//first loop
        for (int i = 1; i <= 5; i++) {
            System.out.println("first " + i);
        }
// second loop
        for (int j = 1; j <= 5; j++) {
            System.out.println("second " + j);
        }
// third loop
        for (int k = 1; k <= 5; k++) {
            System.out.println("third " + k);
        }
    } // end main
} // end class